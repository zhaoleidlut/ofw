package com.htong.alg;

/**
 * 坐标点
 * @author 赵磊
 *
 */
public class Point_old{
	private float x;	//X坐标
	private float y;	//Y坐标
	private double arCos;	//与P0点的角度
	
	public float getX() {
		return x;
	}
	public void setX(float x) {
		this.x = x;
	}
	public float getY() {
		return y;
	}
	public void setY(float y) {
		this.y = y;
	}
	public double getArCos() {
		return arCos;
	}
	public void setArCos(double arCos) {
		this.arCos = arCos;
	}

}

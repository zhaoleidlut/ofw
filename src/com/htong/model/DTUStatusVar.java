package com.htong.model;

public class DTUStatusVar {
	private String commStatus;
	private String connStatus;
	private String dtuNum;
	private String heartBeatTime;
	protected String wellNum;
	public String getCommStatus() {
		return commStatus;
	}
	public void setCommStatus(String commStatus) {
		this.commStatus = commStatus;
	}
	public String getConnStatus() {
		return connStatus;
	}
	public void setConnStatus(String connStatus) {
		this.connStatus = connStatus;
	}
	public String getDtuNum() {
		return dtuNum;
	}
	public void setDtuNum(String dtuNum) {
		this.dtuNum = dtuNum;
	}
	public String getHeartBeatTime() {
		return heartBeatTime;
	}
	public void setHeartBeatTime(String heartBeatTime) {
		this.heartBeatTime = heartBeatTime;
	}
	public String getWellNum() {
		return wellNum;
	}
	public void setWellNum(String wellNum) {
		this.wellNum = wellNum;
	}
	
	

}
